    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        String money = (String) cbxMoney.getSelectedItem();
        String amount = (String) cbxAmount.getSelectedItem();
        String date = (String) cbxDate.getSelectedItem();
        if (!money.equals("--All--")) {
            if (money.equals("Cao nhất")) {
                fillTable(daoStatisticalDao.select_receipt("MoneyDESC"));
            } else if (money.equals("Thấp nhất")) {
                fillTable(daoStatisticalDao.select_receipt("MoneyASC"));
            }
        }
        if (!amount.equals("--All--")) {
            if (amount.equals("Giảm dần")) {
                fillTable(daoStatisticalDao.select_receipt("AmountDESC"));
            } else if (amount.equals("Tăng dần")) {
                fillTable(daoStatisticalDao.select_receipt("AmountASC"));
            }
        }
        if (!date.equals("--All--")) {
            if (date.equals("Gần nhất")) {
                fillTable(daoStatisticalDao.select_receipt("DateDESC"));
            } else if (date.equals("Xa nhất")) {
                fillTable(daoStatisticalDao.select_receipt("DateASC"));
            }
        }
    }//GEN-LAST:event_btnSearchActionPerformed

