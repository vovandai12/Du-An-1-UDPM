    //load all data
    private void fillTable(List<Object[]> list) {
        try {
            DefaultTableModel model = (DefaultTableModel) tableReciept.getModel();
            model.setRowCount(0);
            for (Object[] object : list) {
                model.addRow(object);
            }
            model.fireTableDataChanged();
        } catch (Exception e) {
            MessageDialogHelper.showErrorDialog(this, "Lỗi dữ liệu bảng : " + e.toString(), "Lỗi");
            System.out.println("*ManagerReceiptJDialog(fillTable) - " + e.toString());
        }
    }

